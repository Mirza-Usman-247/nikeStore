import mongoose, { models, Schema } from "mongoose";

const registerUserSchema = new Schema(
  {
    firstName: {
      type: String,
      required: true,
    },
    lastName: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const registerUser = models["registerUser"] || mongoose.model("registerUser", registerUserSchema);

export default registerUser;
